function renderCards() {
  const serviceGrid = document.getElementById("serviceGrid");
  serviceGrid.innerHTML = services
    .map(
      (service, index) => `
      <button class="service-card" data-service="${index}">
        <span class="service-image service-tone-${(index % 5) + 1}">
          <span class="service-number">${String(index + 1).padStart(2, "0")}</span>
          <span class="service-icon">${service.icon}</span>
        </span>
        <span class="service-body">
          <h3>${localizedTitle(service)}</h3>
          <p>${localizedService(service).text}</p>
          <span class="service-tags">${localizedService(service).items
            .slice(0, 3)
            .map((item) => `<em>${item}</em>`)
            .join("")}</span>
        </span>
        <span class="service-action">${t("learnMore")}</span>
      </button>`
    )
    .join("");

  const newsList = document.getElementById("expoNewsList");
  if (newsList) {
    newsList.innerHTML = [...exhibitionNews]
      .sort((a, b) => new Date(a.date) - new Date(b.date))
      .map(
        (news) => `
        <button class="news-item" data-news="${news.title}">
          <span class="news-image">${fairVisual(news.fair)}<span class="news-label">${news.fair.split(" ")[0]}</span></span>
          <span class="news-copy">
            <time>${news.date} · ${news.sourceName}</time>
            <strong>${currentLang === "zh" ? news.zh : news.title}</strong>
            <span>${currentLang === "zh" ? news.zhText : news.text}</span>
            <em>${currentLang === "zh" ? "查看官方资讯与 NJ GROUP 动作" : "View official insight and NJ GROUP action"}</em>
          </span>
        </button>`
      )
      .join("");
  }

  const calendar = document.getElementById("expoCalendar");
  if (calendar) {
    calendar.innerHTML = exhibitionCalendar
      .map(
        (fair, index) => `
        <button class="fair-card" data-fair="${index}">
          <span class="fair-date">${fair.date}</span>
          <span class="fair-visual" aria-hidden="true">${fairVisual(fair.name)}</span>
          <h3>${fair.name}</h3>
          <p>${currentLang === "zh" ? fair.zhSector : fair.sector}</p>
          <small>${fair.venue}</small>
        </button>`
      )
      .join("");
  }
}

function drawerItem(item) {
  if (typeof item === "string") {
    return `<li>${item}</li>`;
  }
  return `<li><a href="${item.url}" target="_blank" rel="noopener">${item.label}</a></li>`;
}

function openDrawer({ title, text, items, kicker = "NJ GROUP" }) {
  document.getElementById("drawerKicker").textContent = kicker;
  document.getElementById("drawerTitle").textContent = title;
  document.getElementById("drawerText").textContent = text;
  document.getElementById("drawerList").innerHTML = items.map(drawerItem).join("");
  document.getElementById("detailDrawer").classList.add("open");
  document.getElementById("detailDrawer").setAttribute("aria-hidden", "false");
}

